			<p><em>Zij schreef:</em><br> Heel veel dank voor de organisatie van dit project. Daar komt veel bij kijken! Jullie hadden het zo zorgzaam voorbereid en verzorgd. Alle mails met de deelnemers, het zoeken van vervangers, de repetitiedagen in de kerk, het organiseren van de pizza’s, het prachtige programmaboekje. Hopelijk is dit voor jullie ook de moeite waard geweest. Ik heb in elk geval erg genoten van het meedoen aan dit project. Nu niet in het orkest, maar in het koor. Het was een voorrecht om onder Mitchell de muziek in te studeren. Heel stimulerend. Willen jullie hem ook bedanken voor de drie zaterdagen, waarin hij ons op zo positieve en professionele wijze begeleidde. Ik heb zijn mailadres helaas niet. Hij heeft zelf ook zo prachtig gezongen als solist! En dan die drie Tsjechische musici die speciaal voor het concert heen en weer kwamen rijden. Ongelooflijk.
			</p>
