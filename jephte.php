  <p>Zijn hele leven moet Montéclair hebben gewerkt als muziekleraar van hoog aanzien: onder zijn leerlingen waren de dochters van zijn collega François Couperin. Zijn benadering van lesgeven was fris en bijna modern. Hij publiceerde boeken over het onderwijzen van muziek (bijvoorbeeld 1709), en rond 1730 publiceerde hij de bundel <em>Recueil de brunettes, </em>die vocale muziek gearrangeerd voor fluit bevat. De collectie was uitdrukkelijk bedoeld als een pedagogisch hulpmiddel om de Franse stijl te leren, en om die reden bevat de muzieknotatie ook de tekst. Hij opende een muziekwinkel in 1721, stopte met lesgeven in 1735 en gaf zijn positie in het operarorkest op kort voor zijn dood. Hij stierf in 1737 in Aumont.</p>
  <p>Montéclair was niet bijzonder productief als componist, maar was een vernieuwer in orkestratie die een belangrijke invloed had op de ontwikkeling van de kunstvorm. Zijn werk werd later overgenomen door Jean-Philippe Rameau. Zijn specialiteit was het gebruik van bepaalde instrumenten om de uitbeelding van de toneelscène te verbeteren, door bijvoorbeeld  hoorns achter het podium zacht te laten spelen om een ​​verre jacht te simuleren.</p>
