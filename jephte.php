  <h2>De bijbelse opera <em>Jephté</em></h2>
  <p><em><strong>Jephté</strong></em> (Jefta) is een opera van de Franse componist Michel Pignolet de Montéclair. Het heeft de vorm van een <em><a href="https://nl.wikipedia.org/wiki/Tragédie_lyrique" target="_blank">tragédie en musique </a></em>in een proloog en vijf aktes (vanwege het onderwerp ervan werd het ook een <em>tragédie biblique</em> genoemd). Het libretto, door  abt Simon-Joseph Pellegrin, is gebaseerd op het bijbelverhaal van <a href="https://nl.wikipedia.org/wiki/Jefta" target="_blank">Jefta</a>, die beloofd de eerste die hij na een gewonnen veldslag ontmoet te offeren aan God en dan zijn blije dochter ontmoet. Het oratorium werd voor het eerst opgevoerd in de Académie royale de musique, Parijs op 28 februari 1732. Het was de eerste opera in Frankrijk die een verhaal uit de Bijbel gebruikte op het openbare toneel. Om deze reden verbood kardinaal De Noailles de uitvoering van het werk voor een tijdje. Montéclair maakte revisies voor hernemingen van het werk in maart 1732 en april 1737.</p>
  <p>In 1735 werd de sopraan Catherine-Nicole Lemaure, die Iphise bij de première had gezongen, gedwongen om in de herneming van dat jaar te verschijnen op straffe van gevangenisstraf als ze dat niet zou doen. Daarom gaf ze opzettelijk een vreselijk slechte uitvoering om de productie te saboteren en werd ze uitgejouwd door het publiek. Ze werd 's nachts opgesloten in een Parijse gevangenis, tot ze de volgende avond terugkeerde om de rol correct te zingen.</p>
  <h2>Michel Pignolet de Montéclair (1667 - 1737)</h2>
  <p>Michel Pignolet de Montéclair werd geboren als Michel Pignolet in een eenvoudige weversfamilie in Andelot, Haute-Marne, Frankrijk. Pas later voegde hij &quot;Montéclair&quot; (de naam van een fort in zijn woonplaats) aan zijn naam toe. Er is weinig bekend over zijn leven en er zijn geen  portretten bekend. Toen hij negen was, werd hij toegelaten tot de koorschool , wat waarschijnlijk zijn enige kans om te ontsnappen aan de armoede van het leven van een wever was. In 1687 ging hij naar Parijs en werd hij aangenomen bij het orkest van de Opera, waar hij de 'base de violon' speelde. In Parijs studeerde hij bij Jean-Baptiste Moreau.</p>
  <p>Op een gegeven moment tussen 1687 en de vroege jaren van de nieuwe eeuw, is hij waarschijnlijk <em>maître de musique </em>geweest van de Prins van Vaudémont en heeft hij hem gevolgd naar Italië. Waarschijnlijk kreeg hij daar het idee om de contrabas toe te voegen aan het opera-orkest.</p>
