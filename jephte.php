<h4><span id="Prologue">Proloog</span></h4>
  <p><em>La Verité </em>(de waarheid) jaagt de valse heidense goden  Apollo, Venus en Polyhymnia weg.</p>
  <h4>Akte een</h4>
  <p>De hogepriester Phinée kiest Jefta als leider van de Israëlieten ,terwijl diezich voorbereiden om de Efraïmieten, de mensen van Efraïm, aan te vallen. Jefta belooft God de eerste persoon te offeren die hij ziet bij zijn terugkeer uit de strijd, als hij de overwinning behaalt.</p>
  <h4>Akte twee</h4>
  <p>De leider van de Efraïmieten, Ammon, wordt gevangen gehouden in het paleis van Jefta. Ammon negeert de aandrang van zijn metgezel Abner om te ontsnappen, omdat hij verliefd is geworden op Jefta's dochter, Iphise. Iphise bekent  haar moeder dat ze ook verliefd is op Ammon. Nieuws arriveert van Jefta's overwinning in de strijd.</p>
  <h4>Akte drie</h4>
  <p>Jefta is geschokt als Iphise de eerste persoon is die hij ziet wanneer hij thuiskomt. Hij vertelt haar over zijn gelofte en zij bereidt zich voor om te worden opgeofferd, ondanks de smeekbeden van Ammon.</p>
  <h4>Akte vier</h4>
  <p>Iphise klaagt over haar lot, maar accepteert het. Ammon zweert dat hij zijn leger zal leiden om haar te redden, maar ze verwerpt zijn aanbod.</p>
  <h4>Akte vijf</h4>
  <p>De Israëlieten bereiden het offer in de tempel voor. Ammon en zijn mannen komen binnen maar ze worden getroffen door een bliksemschicht uit de hemel. De priester Phinéé verklaart dat God tevreden is met Iphise en dat haar leven wordt gespaard.</p>
  <h3>Bladmuziek</h3>
  <p>De partituur van <em>Jephté</em> is bewaard gebleven in een gedrukte editie uit het jaar 1732, die beschikbaar is op <a href="https://imslp.org/wiki/Jepht%C3%A9_(Mont%C3%A9clair%2C_Michel_Pignolet_de)" target="_blank">IMSLP</a>. Hij werd gedrukt en gepubliceerd door François Boivin, een neef van Montéclair. De totale duur van de muziek is ongeveer 150 minuten, dus voor dit project zullen we een verkorte versie maken van ongeveer een uur  muziek, een selectie die zich concentreert op de aria's en koren. Een collectief van docenten en deelnemers transcribeert de partituur in moderne notatie en maakt makkelijk leesbare partijen.   </p>
